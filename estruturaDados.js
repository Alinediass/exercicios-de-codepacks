// Fila
let fila = [3, 7, 9, 1, 0];
while (fila.length > 0) {
  let numeroRemovido = fila.shift();
  console.log(numeroRemovido);
}

// Lista
let lista = [3, 7, 9, 1, 0];
while (lista.length > 0) {
  let numeroRemovido = lista.pop();
  console.log(numeroRemovido);
}

// Pilha
let pilha = [3, 7, 9, 1, 0];
while (pilha.length > 0) {
  let numeroRemovido = pilha.pop();
  console.log(numeroRemovido);
}
